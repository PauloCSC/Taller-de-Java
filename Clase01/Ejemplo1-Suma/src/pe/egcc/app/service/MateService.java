package pe.egcc.app.service;

public class MateService {
    /**
     * Calcula la suma de los dos parametros
     * @param n1 Numero 1
     * @param n2 Numero 2
     * @return la suma de n1 y n2
     */
    public int sumar(int n1, int n2){
        return (n1+n2);
    }
    
    public int restar(int n1, int n2){
        return (n1-n2);
    }
    
}
